export const STAGE_COLORS: Record<string, string> = {
  egg: '#a78bfa', facehugger: '#22d3ee',
  chestburster: '#f97316', xenomorph: '#22d3ee', dead: '#6b7280',
}

export function getTamaMood(pet: any): 'happy' | 'neutral' | 'sad' | 'dying' {
  if (pet.stage === 'dead') return 'dying'
  if (pet.health < 15) return 'dying'
  if (pet.hunger > 70 || pet.happiness < 30 || pet.health < 30) return 'sad'
  if (pet.hunger < 40 && pet.happiness > 60 && pet.health > 70) return 'happy'
  return 'neutral'
}

export function getTamaFrameKey(pet: any): string {
  if (pet.stage === 'dead') return 'dead'
  const mood = getTamaMood(pet)
  const key  = `${pet.stage}_${mood}`
  return TAMA_FRAMES[key] ? key : `${pet.stage}_neutral`
}
